// Modulo

// If I divide the first number by the second number, what is the remainder?

// 4 / 2 = 2
console.log(4 / 2);

// With a remainder of 0
console.log(4 % 2);

// 7 / 2 = 3.5
console.log(7 / 2);

// With a remainder of 1
console.log(7 % 2);

// If remainder is equal to 0 then the first number is s a multiple of the second number

console.log(8 % 2 === 0);
// true

console.log(9 % 2 === 0);
// false